//Sum of All the Element in the Array 
package arrays2;
public class Arrays2 {
    public static void main(String[] args) {
        int Array[]={3,9,7,8,12,6,15,5,4,10},S=0;
         for (int i=0;i<Array.length;i++){
                S =S+Array[i];}
         System.out.println(S);
    }
    
}
