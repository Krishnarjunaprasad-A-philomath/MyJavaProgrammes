package ifelse;
public class IfElse {
    public static void main(String[] args) {
       int a=10,b=15,c=20;
       if (a<b)
       {
           System.out.println("a is smaller than the b");
       }
       else if (a>c)
       {
           System.out.println(" a is greater than the b");
       }
       else
       {
           System.out.println("a is smaller than the b");
       }
    }
    
}
