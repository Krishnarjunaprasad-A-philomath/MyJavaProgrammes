//Practcing Increment and decrement operators
import java.lang.*;
import java.util.Scanner;
class IncrementDecrements
{
	public static void main(String arg[])
	{
		int X=10;
		int Y=X++;
		int Z=++Y;
		System.out.println(X++);
		System.out.println(++X);
		System.out.println(Y);
		System.out.println(Z);
		
	}
}